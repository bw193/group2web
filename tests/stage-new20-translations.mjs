function translateValue(locale, value) {
  let out = String(value ?? '').trim();
  const rules = valueReplacements[locale] ?? [];
  for (const [from, to] of rules) out = out.replace(from, to);
  return out;
}

function buildFullDescription(locale, name, productFocus) {
  return copy[locale].sections
    .map(([heading, body], i) => `<p><strong>${i + 1}. ${heading}</strong></p><p>${body(name, productFocus)}</p>`)
    .join('');
}

